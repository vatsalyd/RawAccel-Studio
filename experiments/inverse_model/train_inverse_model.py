"""
Train the InverseCurveNet on synthetic data generated by gen_synthetic_data.py.

Features:
- Train / val split with early stopping
- LR scheduling (ReduceLROnPlateau)
- Curve L2 distance metric alongside param MSE
- TensorBoard logging

Usage:
    python -m experiments.inverse_model.train_inverse_model
"""
import argparse
import os

import numpy as np
import torch
from torch import nn
from torch.optim.lr_scheduler import ReduceLROnPlateau
from torch.utils.data import DataLoader

from data.utils import SequenceDataset
from models.inverse_curve_net import InverseCurveNet
from models.curve_param_config import (
    sensitivity_from_speed,
    tensor_to_params,
    TUNE_KEYS,
)

try:
    from torch.utils.tensorboard import SummaryWriter
except ImportError:
    SummaryWriter = None


# ---------------------------------------------------------------------------
# Curve L2 distance metric
# ---------------------------------------------------------------------------

def curve_l2_distance(
    pred_params: torch.Tensor,
    true_params: torch.Tensor,
    speed_points: int = 50,
) -> torch.Tensor:
    """
    Compute mean L2 distance between predicted and true sensitivity curves,
    evaluated at `speed_points` evenly spaced mouse speeds.

    pred_params, true_params: (B, P)
    """
    speeds = torch.linspace(1.0, 3000.0, speed_points)  # counts/s
    dists = []
    for i in range(pred_params.size(0)):
        p_pred = tensor_to_params(pred_params[i], keys=TUNE_KEYS)
        p_true = tensor_to_params(true_params[i], keys=TUNE_KEYS)
        s_pred = sensitivity_from_speed(speeds, p_pred)
        s_true = sensitivity_from_speed(speeds, p_true)
        dists.append(torch.mean((s_pred - s_true) ** 2))
    return torch.mean(torch.stack(dists))


# ---------------------------------------------------------------------------
# Data loading
# ---------------------------------------------------------------------------

def load_dataset(path: str, max_len: int = 80) -> SequenceDataset:
    data = np.load(path, allow_pickle=True)
    sequences = list(data["sequences"])
    params = data["params"]
    return SequenceDataset(sequences, params, max_len=max_len)


# ---------------------------------------------------------------------------
# Training
# ---------------------------------------------------------------------------

def main(config_path: str = None):
    # Defaults (can be overridden by config later)
    data_dir = os.path.join("data", "sim_inverse")
    max_len = 80
    batch_size = 32
    epochs = 50
    lr = 1e-3
    lr_patience = 5
    lr_factor = 0.5
    early_stop_patience = 10
    save_dir = os.path.join("runs", "inverse_model")
    tb_dir = os.path.join(save_dir, "tb")

    os.makedirs(save_dir, exist_ok=True)

    # Load data
    train_path = os.path.join(data_dir, "train.npz")
    val_path = os.path.join(data_dir, "val.npz")

    if not os.path.isfile(train_path):
        print(f"Data not found at {train_path}. Run gen_synthetic_data.py first.")
        return

    train_ds = load_dataset(train_path, max_len)
    val_ds = load_dataset(val_path, max_len)
    train_loader = DataLoader(train_ds, batch_size=batch_size, shuffle=True)
    val_loader = DataLoader(val_ds, batch_size=batch_size, shuffle=False)

    device = torch.device("cuda" if torch.cuda.is_available() else "cpu")
    model = InverseCurveNet(
        input_dim=3,
        num_params=5,
        use_attention=True,
        dropout=0.1,
    ).to(device)

    optim = torch.optim.Adam(model.parameters(), lr=lr, weight_decay=1e-4)
    scheduler = ReduceLROnPlateau(optim, patience=lr_patience, factor=lr_factor)
    loss_fn = nn.MSELoss()

    writer = SummaryWriter(tb_dir) if SummaryWriter else None
    best_val_loss = float("inf")
    epochs_no_improve = 0

    for epoch in range(1, epochs + 1):
        # --- Train ---
        model.train()
        train_loss = 0.0
        for x, y in train_loader:
            x, y = x.to(device), y.to(device)
            pred = model(x)
            loss = loss_fn(pred, y)
            optim.zero_grad()
            loss.backward()
            optim.step()
            train_loss += loss.item() * x.size(0)
        train_loss /= len(train_ds)

        # --- Validate ---
        model.eval()
        val_loss = 0.0
        val_curve_l2 = 0.0
        n_batches = 0
        with torch.no_grad():
            for x, y in val_loader:
                x, y = x.to(device), y.to(device)
                pred = model(x)
                val_loss += loss_fn(pred, y).item() * x.size(0)
                val_curve_l2 += curve_l2_distance(pred.cpu(), y.cpu()).item()
                n_batches += 1
        val_loss /= len(val_ds)
        val_curve_l2 /= max(n_batches, 1)

        scheduler.step(val_loss)
        current_lr = optim.param_groups[0]["lr"]

        print(
            f"Epoch {epoch:3d}: train_loss={train_loss:.5f}  "
            f"val_loss={val_loss:.5f}  curve_L2={val_curve_l2:.5f}  "
            f"lr={current_lr:.1e}"
        )

        if writer:
            writer.add_scalar("loss/train", train_loss, epoch)
            writer.add_scalar("loss/val", val_loss, epoch)
            writer.add_scalar("metrics/curve_l2", val_curve_l2, epoch)
            writer.add_scalar("lr", current_lr, epoch)

        # Early stopping
        if val_loss < best_val_loss:
            best_val_loss = val_loss
            epochs_no_improve = 0
            torch.save(model.state_dict(), os.path.join(save_dir, "inverse_curve_net_best.pt"))
        else:
            epochs_no_improve += 1
            if epochs_no_improve >= early_stop_patience:
                print(f"Early stopping at epoch {epoch} (no improvement for {early_stop_patience} epochs)")
                break

    # Save final model
    torch.save(model.state_dict(), os.path.join(save_dir, "inverse_curve_net.pt"))
    print(f"Models saved to {save_dir}")
    if writer:
        writer.close()


if __name__ == "__main__":
    parser = argparse.ArgumentParser(description="Train InverseCurveNet")
    parser.add_argument("--config", type=str, default=None)
    args = parser.parse_args()
    main(config_path=args.config)